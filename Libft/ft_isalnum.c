#include "libft.h"

int	ft_isalnum(int arg)
{
	if (('a' <= arg && 'z' >= arg) || ('A' <= arg && arg <= 'Z') || \
			('0' <= arg && '9' >= arg))
		return (8);
	return (0);
}
